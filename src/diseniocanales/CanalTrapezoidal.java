    private void calcularMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_calcularMouseClicked
        // TODO add your handling code here:
        
        boolean validaNumeros = true;
        if(isNumeric(this.jCaudal.getText())!= true){
            JOptionPane.showMessageDialog(null, "Ingrese un valor numÃ©rico en el campo Caudal");
            validaNumeros = false;
        } 
        
        if(isNumeric(this.jVelocidad.getText())!= true){
            JOptionPane.showMessageDialog(null, "Ingrese un valor numÃ©rico en el campo de Velocidad");
             validaNumeros = false;
        }
       
        if( validaNumeros){
            float caudal = Float.parseFloat(this.jCaudal.getText());
            float velocidad = Float.parseFloat(this.jVelocidad.getText());
            final int TALUD = 1;
            final float RUGOSIDAD =  (float) 0.02;
            float area = (float) (caudal / velocidad);
            float base = (float) Math.sqrt(area/2);
            float tirante = base;
            float profundidad = (float) (tirante * 1.25); 
            float radioHidraulico = (float) (area / (base + (2 * tirante) * Math.sqrt(2))); 

            float pendiente = (float) Math.pow(velocidad * RUGOSIDAD / (float) Math.pow(radioHidraulico,0.6666),2);

            this.jBase.setText(this.df.format(base));
            this.jProfundidad.setText(this.df.format(profundidad));
            this.jPendiente.setText(this.df.format(pendiente));
            this.jRadioHidraulico.setText(this.df.format(radioHidraulico));

             if(velocidad >= 1){
                JOptionPane.showMessageDialog(null,"El canal debe ser revestido");
            }else{
                JOptionPane.showMessageDialog(null, "El canal puede ser construido en condiciones naturales");
            }
        }
    }//GEN-LAST:event_calcularMouseClicked
private static boolean isNumeric(String cadena){
	try {
		Float.parseFloat(cadena);
		return true;
	} catch (NumberFormatException nfe){
		return false;
	}
}
